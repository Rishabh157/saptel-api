exports.get = async (req, res) => {
  try {
    let companyId = req.params.companyid;

    //if no default query then pass {}
    let matchQuery = {
      companyId: new mongoose.Types.ObjectId(companyId),
      isDeleted: false,
    };
    if (req.query && Object.keys(req.query).length) {
      matchQuery = getQuery(matchQuery, req.query);
    }

    // let dataExist = await featureService.findAllWithQuery(matchQuery);
    let dataExist = await featureService.aggregateQuery([
      {
        $group: {
          _id: { moduleName: "$moduleName", moduleRank: "$moduleRank" },
          features: {
            $addToSet: {
              feature_id: "$_id",
              featureName: "$featureName",
              moduleName: "$moduleName",
              featureRank: "$featureRank",
              moduleRank: "$moduleRank",
              accessModules: "$accessModules",
            },
          },
        },
      },
      {
        $project: {
          moduleName: "$_id.moduleName",
          moduleRank: "$_id.moduleRank",
          features: {
            $sortArray: {
              input: "$features",
              sortBy: { featureRank: 1 },
            },
          },
          _id: 0,
        },
      },
      { $sort: { moduleRank: 1 } },
    ]);

    if (!dataExist || !dataExist.length) {
      throw new ApiError(httpStatus.OK, "Data not found.");
    } else {
      return res.status(httpStatus.OK).send({
        message: "Successfull.",
        status: true,
        data: dataExist,
        code: "OK",
        issue: null,
      });
    }
  } catch (err) {
    let errData = errorRes(err);
    logger.info(errData.resData);
    let { message, status, data, code, issue } = errData.resData;
    return res
      .status(errData.statusCode)
      .send({ message, status, data, code, issue });
  }
};
