//add start
exports.add = async (req, res) => {
  try {
    let { featureName, moduleName, featureRank, moduleRank, accessModule } =
      req.body;
    /**
     * check duplicate exist
     */
    req.body.accessModules = await Promise.all(
      accessModule.map(async (ele) => {
        let isAccessExists = await accessmoduleService.getOneByMultiField({
          _id: ele.accessModuleId,
        });
        if (!isAccessExists) {
          throw new ApiError(httpStatus.OK, "Invalid accessModule ID");
        }
        return ele;
      })
    );

    let isModuleNameExists = await featureService.findAllWithQuery({
      moduleName: moduleName,
      isDeleted: false,
    });
    if (
      isModuleNameExists.length &&
      isModuleNameExists[0].moduleRank !== moduleRank
    ) {
      throw new ApiError(httpStatus.OK, "moduleRank should be same");
    }

    let dataExist = await featureService.isExists(
      [{ featureName }, { moduleName }],
      false,
      true
    );
    if (dataExist.exists && dataExist.existsSummary) {
      throw new ApiError(httpStatus.OK, dataExist.existsSummary);
    }
    //------------------create data-------------------
    let dataCreated = await featureService.createNewData({ ...req.body });

    if (dataCreated) {
      return res.status(httpStatus.CREATED).send({
        message: "Added successfully.",
        data: dataCreated,
        status: true,
        code: "CREATED",
        issue: null,
      });
    } else {
      throw new ApiError(httpStatus.NOT_IMPLEMENTED, `Something went wrong.`);
    }
  } catch (err) {
    let errData = errorRes(err);
    logger.info(errData.resData);
    let { message, status, data, code, issue } = errData.resData;
    return res
      .status(errData.statusCode)
      .send({ message, status, data, code, issue });
  }
};

